#equal to
print(5 == 6)
print(2 == 2.)
print()

#greater than
n = 500 > 75
print(n)
print(25 >= 25)
print()

#less than
print(5 < 2)
print(-3 < -1)  #the larger the value of the negative number, the smaller it is (further away from 0)
print()

#not equal to
v = 4 != 4.  #python converts the int into its real equivalent
print(v)
print(5 != 50)
print()